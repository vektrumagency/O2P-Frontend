import { useState } from 'react'

const COLORS = [
  '#FF6B6B', '#FFD93D', '#6BCB77', '#4D96FF',
  '#FF6FC8', '#FF9A3C', '#A78BFA', '#06D6A0',
]

function randomColor() {
  return COLORS[Math.floor(Math.random() * COLORS.length)]
}

function AnimatedChar({ char }: { char: string }) {
  const [color, setColor] = useState('#0a0a0a')
  const [hovered, setHovered] = useState(false)
  if (char === ' ') return <span>&nbsp;</span>
  return (
    <span
      style={{ color, transition: hovered ? 'color 0.1s' : 'color 6s ease-out', cursor: 'default' }}
      onMouseEnter={() => { setHovered(true); setColor(randomColor()) }}
      onMouseLeave={() => { setHovered(false); setColor('#0a0a0a') }}
    >
      {char}
    </span>
  )
}

function AnimatedWord({ word }: { word: string }) {
  return (
    <span style={{ whiteSpace: 'nowrap' }}>
      {word.split('').map((char, i) => (
        <AnimatedChar key={i} char={char} />
      ))}
    </span>
  )
}

